#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	a;

	a = 0;
	while (s[a] != '\0')
		a++;
	if (c == '\0')
		a++;
	while (a--)
		if (s[a] == c || s[a] == '\0')
			return ((char*)&s[a]);
	return (NULL);
}