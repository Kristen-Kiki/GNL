#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i = 0;
	int x = ft_strlen(s1);
	while (s2[i] && n > 0)
	{
		s1[x + i] = s2[i];
		i++;
		n--;
	}
	s1[x + i] = '\0';
	return (s1);
}