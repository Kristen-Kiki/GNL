#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *dst = s1;
	const char *src = s2;

	while (*dst)
		dst++;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (s1);
}
