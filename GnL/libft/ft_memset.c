#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *s;

	s = (unsigned char *)b;
	if (len == 0)
		return (b);
	while (len--)
		*s++ = (unsigned char)c;
	return (b);
}
